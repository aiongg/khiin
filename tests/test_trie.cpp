#define BOOST_TEST_DYN_LINK
#include <boost/test/unit_test.hpp>

#include "trie.h"

using namespace std;
using namespace TaiKey;

BOOST_AUTO_TEST_SUITE(TrieTest);

BOOST_AUTO_TEST_CASE(Trie_Search) {
    TNode *root = new TNode;
    root->insert(u8"niau");
    bool res = root->searchExact(u8"niau");

    BOOST_TEST(res == true);
    delete root;
}

BOOST_AUTO_TEST_CASE(Trie_Remove) {
    TNode *root = new TNode;
    root->insert(u8"niau");
    root->remove(u8"niau");

    BOOST_TEST(!root->searchExact(u8"niau"));
    BOOST_TEST(!root->searchExact(u8"nia"));
    BOOST_TEST(!root->searchExact(u8"ni"));
    BOOST_TEST(!root->searchExact(u8"n"));

    delete root;
}

BOOST_AUTO_TEST_CASE(Trie_Prefix) {
    TNode *root = new TNode;
    root->insert(u8"niau");
    BOOST_TEST(root->isPrefix(u8"nia") == true);
    delete root;
}

BOOST_AUTO_TEST_CASE(Trie_AutoComplete) {
    TNode *root = new TNode;
    root->insert(u8"niau");
    root->insert(u8"nia");
    root->insert(u8"na");

    std::vector<std::string> res = root->autocomplete(u8"n");
    BOOST_TEST(res.size() == 3);
    BOOST_TEST((std::find(res.begin(), res.end(), "niau") != res.end()));
    BOOST_TEST((std::find(res.begin(), res.end(), "nia") != res.end()));
    BOOST_TEST((std::find(res.begin(), res.end(), "na") != res.end()));

    res = root->autocomplete(u8"na");
    BOOST_TEST(res.size() == 1);
    BOOST_TEST((std::find(res.begin(), res.end(), "na") != res.end()));

    delete root;
}

BOOST_AUTO_TEST_CASE(Trie_AutoCompleteTone) {
    TNode *root = new TNode;
    root->insert(u8"na2");
    root->insert(u8"na7");
    root->insert(u8"nai");

    std::vector<std::string> res = root->autocompleteTone(u8"na");
    BOOST_TEST(res.size() == 2);
    BOOST_TEST((std::find(res.begin(), res.end(), "na2") != res.end()));
    BOOST_TEST((std::find(res.begin(), res.end(), "na7") != res.end()));

    delete root;
}

